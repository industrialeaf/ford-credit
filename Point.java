/** Represents a Point object, which is used to store the x and y-coordinates of a point.
 *  @author Jonah Noh
 */

public class Point {

    /** Represents the x-coordinate of the Point. */
    public final int x;
    /** Represents the y-coordinate of the Point. */
    public final int y;

    /** Constructor for initializing a new Point. */
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
